    </li>
  </ul>

  <h2>2. General Hackathon Rules</h2>
  <ul>
    <li>
      <strong>Team Formation:</strong> Teams can consist of up to 3-4 members. Participants
      are encouraged to form teams before or during the event. Solo participants
      are also welcome.
    </li>
    <li>
      <strong>Project Submissions:</strong> All projects must be developed during
      the hackathon timeframe. Pre-existing projects, partially completed work, or
      any prior submissions will not be accepted.
    </li>
    <li>
      <strong>Intellectual Property:</strong> Participants retain ownership of their
      projects, but submissions must not contain any material that violates intellectual
      property rights, such as copyrighted software, music, or images.
    </li>
    <li>
      <strong>Judging Criteria:</strong> Projects will be judged based on innovation,
      technical difficulty, design, impact, and completeness, and other criteria
      specified by the Organizer.
    </li>
    <li>
      <strong>Event Conduct:</strong> Participants are expected to respect all event
      rules and comply with instructions from the Organizer and staff. Disruptive
      behavior will lead to disqualification.
    </li>
    <li>
      <strong>Venue Regulations:</strong> Participants must follow all venue-specific
      rules and guidelines, including safety protocols, designated areas, and permitted
      equipment use.
    </li>
  </ul>

  <h2>3. Acceptable and Unacceptable Behavior</h2>
  <ul>
    <li>
      <strong>Acceptable Behavior:</strong> Encouraging and supporting fellow participants,
      sharing knowledge, collaborating within teams, following the event schedule,
      and using hackathon resources responsibly.
    </li>
    <li>
      <strong>Unacceptable Behavior:</strong> Harassment, spamming, vandalism, theft,
      deliberate disruption of talks or workshops, inappropriate language or imagery
      in projects, and unauthorized use of others' property.
    </li>
    <li>
      <strong>Alcohol and Substance Use:</strong> The consumption of alcohol or illegal
      substances is strictly prohibited. Participants who are found under the influence
      will be removed from the event.
    </li>
    <li>
      <strong>Privacy and Recording:</strong> Do not photograph or record participants
      without their consent. Respect the privacy of others at all times.
    </li>
  </ul>

  <h2>4. Project Development Guidelines</h2>
  <ul>
    <li>
      <strong>Timeframe:</strong> Projects must be developed exclusively during the
      hackathon, beginning at the official start time and ending at the submission
      deadline.
    </li>
    <li>
      <strong>Use of External Code:</strong> The use of open-source libraries and
      frameworks is permitted, but projects must include significant original work
      created during the hackathon.
    </li>
    <li>
      <strong>Team Membership Changes:</strong> Teams must be finalized before the
      hackathon ends. Any changes must be reported to the Organizer immediately.
    </li>
  </ul>

  <h2>5. Safety and Security</h2>
  <ul>
    <li>
      <strong>Emergency Procedures:</strong> In the event of an emergency, follow
      the directions provided by the Organizer and venue staff. Contact security
      or local authorities if necessary.
    </li>
    <li>
      <strong>Health and Safety:</strong> Participants should prioritize their health,
      avoid overexertion, and take regular breaks. Sleeping areas may be provided;
      use them if needed.
    </li>
  </ul>

  <h2>6. Reporting Violations</h2>
  <ul>
    <li>
      If you encounter behavior that violates these Terms or makes you
      uncomfortable, report it to the Organizer using the designated reporting
      channels. Reports can be made anonymously.
    </li>
    <li>Contact details for reporting: contact@ubhacking.com.</li>
  </ul>

