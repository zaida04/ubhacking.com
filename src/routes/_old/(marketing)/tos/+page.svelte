      behavior towards any participant will not be tolerated.
    </li>
    <li>
      Unacceptable behavior includes but is not limited to offensive comments,
      intimidation, stalking, inappropriate physical contact, sexualized
      behavior, and discrimination based on gender, race, age, disability, or
      other personal attributes.
    </li>
    <li>
