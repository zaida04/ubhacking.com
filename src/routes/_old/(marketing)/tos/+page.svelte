      safety.
    </li>
    <li>
      The Organizer reserves the right to cancel, modify, or postpone the
      hackathon for any reason, including health and safety concerns.
    </li>
  </ul>

  <h2>8. Acceptance of Terms</h2>
  <p>
    By participating in UB Hacking, you agree to these Terms and to adhere to
    the rules outlined for a safe and respectful event. Violations may result in
    disqualification and/or expulsion from the hackathon.
